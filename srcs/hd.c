#include "push_swap.h"

void	hd(t_ps **head, char **av, unsigned short *f)
{
	(*head)->n = ft_atoi(av[++(*f)]);
	(*head)->c = 1;
	(*head)->prv = NULL;
	(*head)->nxt = NULL;
}
