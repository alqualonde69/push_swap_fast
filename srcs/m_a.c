#include "push_swap.h"

void	m_a(t_ps *a, int *mx, int *mn)
{
	*mn = a->n;
	*mx = a->n;
	while (a && a->c)
	{
		a->n < *mn ? *mn = a->n : 0;
		a->n > *mx ? *mx = a->n : 0;
		a = a->nxt;
	}
}
