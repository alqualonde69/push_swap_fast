#include "push_swap.h"

int		issort2(t_ps *a)
{
	int		frst;
	int		mn;
	int		mx;

	m_a(a, &mx, &mn);
	frst = a->n;
	while (a->nxt && a->nxt->c)
	{
		if (a->n > a->nxt->n && a->n != mx)
			return (0);
		a = a->nxt;
	}
	return (a->n > frst && a->n != mx ? 0 : 1);
}
