#include "push_swap.h"

int		is_nu(char **av, int i)
{
	int		j;

	while (av[++i])
	{
		if ((av[i][0] < '0' || av[i][0] > '9') && av[i][0] != '-' &&
			av[i][0] != '+')
			return (1);
		if ((av[i][0] == '-' || av[i][0] == '+') && !av[i][1])
			return (1);
		j = 0;
		while (av[i][++j])
			if (av[i][j] < '0' || av[i][j] > '9')
				return (1);
	}
	return (0);
}
