#include "push_swap.h"

int		isdup(t_ps *a)
{
	t_ps	*b;

	while (a->nxt)
	{
		b = a->nxt;
		while (b)
		{
			if (a->n == b->n)
				return (freetps(a));
			b = b->nxt;
		}
		a = a->nxt;
	}
	return (0);
}
